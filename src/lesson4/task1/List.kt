fun convertToString(n: Int, base: Int): String =
        convert(n, base).joinToString(
                separator = "",
                transform = { if (it > 9) ('a' + it - 10).toString() else "$it" })


/**
 * Средняя
 *
 * Перевести число, представленное списком цифр digits от старшей к младшей,
 * из системы счисления с основанием base в десятичную.
 * Например: digits = (1, 3, 12), base = 14 -> 250
 */
fun decimal(digits: List<Int>, base: Int): Int {
    var q = 0.0
    var p = 0.0
    for (i in digits.size - 1 downTo 0) {
        q += digits[i] * pow(base.toDouble(), p)
        p++
    }
    return q.toInt()
}

/**
 * Сложная
 *
 * Перевести число, представленное цифровой строкой str,
 * из системы счисления с основанием base в десятичную.
 * Цифры более 9 представляются латинскими строчными буквами:
 * 10 -> a, 11 -> b, 12 -> c и так далее.
 * Например: str = "13c", base = 14 -> 250
 */
fun decimalFromString(str: String, base: Int): Int {
    val list = mutableListOf<Int>()
    for (i in str) {
        if (i <= '9') list.add(i - '0')
        else list.add(i + 10 - 'a')
    }
    return decimal(list, base)
}


/**
 * Сложная
 *
 * Перевести натуральное число n > 0 в римскую систему.
 * Римские цифры: 1 = I, 4 = IV, 5 = V, 9 = IX, 10 = X, 40 = XL, 50 = L,
 * 90 = XC, 100 = C, 400 = CD, 500 = D, 900 = CM, 1000 = M.
 * Например: 23 = XXIII, 44 = XLIV, 100 = C
 */
val romeList = listOf("I", "V", "X", "L", "C", "D", "M")

fun roman(n: Int): String {
    var q = n
    var result = romanDigit(q, 0) + romanDigit(q / 10, 2) + romanDigit(q / 100, 4)
    if (q > 999) {
        while (q > 0) {
            q -= 1000
            result += romeList[6]
            if (q < 999) break
        }
    }
    return result.reversed()
}

fun romanDigit(q: Int, p: Int): String {
    return when (q % 10) {
        1 -> romeList[p]
        2 -> romeList[p] + romeList[p]
        3 -> romeList[p] + romeList[p] + romeList[p]
        4 -> romeList[p + 1] + romeList[p]
        5 -> romeList[p + 1]
        6 -> romeList[p] + romeList[p + 1]
        7 -> romeList[p] + romeList[p] + romeList[p + 1]
        8 -> romeList[p] + romeList[p] + romeList[p] + romeList[p + 1]
        9 -> romeList[p + 2] + romeList[p]
        else -> ""
    }
}

/**
 * Очень сложная
 *
 * Записать заданное натуральное число 1..999999 прописью по-русски.
 * Например, 375 = "триста семьдесят пять",
 * 23964 = "двадцать три тысячи девятьсот шестьдесят четыре"
 */
fun russian(n: Int): String {
    var result = ""
    val num = listOf(n % 10, n / 10 % 10, n / 100 % 10, n / 1000 % 10, n / 10000 % 10, n / 100000)
    val first = listOf("", " один", " два", " три", " четыре", " пять",
            " шесть", " семь", " восемь", " девять", " две", " одна")
    val second = listOf(" десять", " одиннадцать", " двенадцать",
            " тринадцать", " четырнадцать", " пятнадцать", " шестнадцать",
            " семнадцать", " восемнадцать", " девятнадцать")
    val third = listOf("", "", " двадцать", " тридцать",
            " сорок", " пятьдесят", " шестьдесят", " семьдесят", " восемьдесят", " девяносто")
    val fourth = listOf("", " сто", " двести", " триста", " четыреста", " пятьсот",
            " шестьсот", " семьсот", " восемьсот", " девятьсот")
    result += fourth[num[5]] +
            if (num[4] == 1) second[num[3]] else third[num[4]] + when (num[4]) {
                1 -> ""
                else -> if (num[3] != 1 && num[3] != 2) first[num[3]] else first[12 - num[3]]
            }
    if (num[3] != 0 || num[4] != 0 || num[5] != 0) result += if (num[4] != 1) when {
        num[3] == 1 -> " тысяча"
        num[3] == 2 || num[3] == 3 || num[3] == 4 -> " тысячи"
        else -> " тысяч"
    } else " тысяч"
    result += fourth[num[2]] + if (num[1] == 1) second[num[0]] else third[num[1]] +
            if (num[1] != 1) first[num[0]] else ""
    return result.trim()
}